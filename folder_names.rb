class WindSpeedComponent
  
  def self.prefix(speed)
    {
      0..3 => "W_0t3", 
      4..7 => "W_4t7", 
      8..12 => "W_8t12", 
      13..17 => "W_13t17",                                         
      18..22 => "W_18t22",
      23..27 => "W_23t27",
      28..35 => "W_28t35",
      35..50 => "W_35t50",
      51..150 => "W_51t150"
    }.detect{|k,v| k === speed}.last
  end
end

